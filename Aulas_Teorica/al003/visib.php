    <h1>O que é Visibilidade?</h1>
    <p>UML => Linguagem de Modelagem Unificada</p>
    <li>Diagrama de classes</li>
    <p>Modificadores de Visibilidade => Indicam o nível de acessos aos componentes internos de uma classe.</p>
    <li>'+' => Representa a visibilidade pública(public) (classe atual e todas as outras tem acesso);</li>
    <li>'-' => Representa a visibilidade Privada(private) (somente a classe atual tem acesso)</li>
    <li>'#' => Representa o modo Protegido(protected) (classe atual e todas as subclasses tem acesso)</li>
